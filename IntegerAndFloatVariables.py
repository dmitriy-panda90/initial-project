# Integer $ Float variables (Numbers)

# print of a simple string variable
numeric_variable = 10
print("Integer variable value is : " + str(numeric_variable))
# Define simple variables for further manipulations
simple_veriable_a = 5
simple_veriable_b = 10

# Summery of 2 variables
summary_result = simple_veriable_a + simple_veriable_b
print("Summery : " +str(summary_result))

# Multiply 2 numbers
multiply_result = simple_veriable_a * simple_veriable_b
print("Multiply result:" + str(multiply_result))

# Devide 2 numbers
devide_result = simple_veriable_a / simple_veriable_b
print("Devide result : " +str(devide_result))

# Power 2 numbers
power_result = simple_veriable_a ** 2
print("Power result : " +str(power_result))

# Modelo action on 2 numbers
modulo_result = simple_veriable_a % 2
print("Modelo result : " +str(modulo_result))

# Decimal point variable -> Float
decimal_variable_a = 2.5
decimal_variable_b = 0.25
summary_of_two_float = decimal_variable_a + decimal_variable_b
print("Summery of two float variables : " +str(summary_of_two_float))

# Apply path actions upon a variable
path_actions_on_variable = 4 + 5
